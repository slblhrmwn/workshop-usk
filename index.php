		<h2>Daya Tarik Cafe Sisi Barat</h2>
		<img src="coffee sisi barat.jpg">
		<img src="foto3.jpg">
		<p>Cafe ini merupakan salah satu dari sekian banyak objek wisata kuliner dan nongkrong di kota Bogor yang akhir-akhir ini viral di perbincangkan diberbagai sosial media. Sisi Barat Coffee ini dapat dibilang masih sangat baru karena Cafe ini baru grand opening pada tanggal 5 Desember 2021 lalu.</p>

		<p>Sisi Barat Coffee ini hadir dengan menyediakan 2 tempat yaitu cafe indoor dan juga outdoor. Cafe ini membawa konsep industrial cafe dengan interior yang simple minimalis serta memberikan kenyamanan bagi pengunjung agar betah menghabiskan waktu di lokasi tersebut.</p> 

		<p>Daya tarik yang ada di Cafe Sisi Barat Coffee ini yaitu tempatnya yang menampilkan pemandangan pesona alam yang indah di sekitar cafe. Hal ini di karenakan lokasinya yang berada tepat disamping Sungai Cisandang Barang dan terdapat banyak pohon yang membuat sejuk di bagian outdoor Sisi Barat Coffee. Wisatawan yang berkunjung akan dapat melihat pemandangan alam berupa aliran sungai dengan suara gemericik air dan membuat hati tenang serta betah untuk berlama-lama di Cafe ini.</p>

		<p>Dari kejauhan anda juga dapat melihat pemandangan Gunung Salak megah dan indah. Selain itu, pepohonan hijau yang rindang dan mengelilingi lokasi cafe Sisi Barat Coffee ini membuat suasana begitu sejuk dan asri. Desain tempatnya yang simpel minimalis serta bagus membuat Cafe ini sangat menarik untuk di foto, terdapat berbagai spot foto menarik yang bagus untuk anda abadikan dan posting di berbagai media sosial. Pastinya siapapun pengunjung Sisi Barat Coffee akan tergoda untuk berfoto di lokasi ini.</p>

		<p>Untuk menu makanan dan minuman di Sisi Barat Coffee ini juga sudah komplit dan memiliki harga yang cukup terjangkau. Hal ini juga menjadi alasan dan daya tarik bagi pengungjung Sisi Barat Coffee.</p> 

